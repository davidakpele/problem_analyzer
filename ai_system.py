class Camera:
    def __init__(self):
        self.cap           = None
        self.face_detected = False
        self.running       = False
        self.frame         = None
        self.cascade       = None
        self.cascade_alt   = None

        if CAM_OK:
            self.cascade = cv2.CascadeClassifier(
                cv2.data.haarcascades + 'haarcascade_frontalface_default.xml')
            self.cascade_alt = cv2.CascadeClassifier(
                cv2.data.haarcascades + 'haarcascade_frontalface_alt2.xml')

    def start(self):
        if not CAM_OK:
            return
        for idx in range(3):
            cap = cv2.VideoCapture(idx)
            if cap.isOpened():
                self.cap = cap
                break
        if self.cap is None:
            return

        self.cap.set(cv2.CAP_PROP_FRAME_WIDTH,  640)
        self.cap.set(cv2.CAP_PROP_FRAME_HEIGHT, 480)
        self.running = True
        threading.Thread(target=self._loop, daemon=True).start()

    def _loop(self):
        while self.running:
            ret, frame = self.cap.read()
            if not ret:
                time.sleep(0.05)
                continue

            display = cv2.resize(frame, (640, 480))
            gray    = cv2.cvtColor(display, cv2.COLOR_BGR2GRAY)
            gray    = cv2.equalizeHist(gray)

            faces = self.cascade.detectMultiScale(
                gray, scaleFactor=1.05, minNeighbors=3,
                minSize=(60, 60), flags=cv2.CASCADE_SCALE_IMAGE)

            if len(faces) == 0 and self.cascade_alt is not None:
                faces = self.cascade_alt.detectMultiScale(
                    gray, scaleFactor=1.05, minNeighbors=3, minSize=(60, 60))

            self.face_detected = len(faces) > 0

            for (x, y, w, h) in faces:
                cv2.rectangle(display, (x, y), (x+w, y+h), (0, 220, 80), 2)
                cv2.putText(display, "ADMIN", (x, y - 8),
                            cv2.FONT_HERSHEY_SIMPLEX, 0.65, (0, 220, 80), 2)

            self.frame = display
            time.sleep(0.03)

    def stop(self):
        self.running = False
        if self.cap:
            self.cap.release()


# ══════════════════════════════════════════════
#  VOICE LISTENER
# ══════════════════════════════════════════════
class VoiceListener:
    def __init__(self, result_queue):
        self.q       = result_queue
        self.running = False
        if SPEECH_OK:
            self.recognizer = sr.Recognizer()
            self.recognizer.energy_threshold         = 300
            self.recognizer.dynamic_energy_threshold = True
            self.recognizer.pause_threshold          = 0.8

    def start(self):
        if not SPEECH_OK:
            return
        self.running = True
        threading.Thread(target=self._loop, daemon=True).start()

    def _loop(self):
        with sr.Microphone() as source:
            self.recognizer.adjust_for_ambient_noise(source, duration=1)
            while self.running:
                try:
                    audio = self.recognizer.listen(source, timeout=4, phrase_time_limit=30)
                    text  = self.recognizer.recognize_google(audio)
                    self.q.put(("voice", text))
                except sr.WaitTimeoutError:
                    pass
                except sr.UnknownValueError:
                    pass
                except Exception as e:
                    self.q.put(("error", str(e)))

    def stop(self):
        self.running = False


