    # ── voice controls ────────────────────────
    def _on_voice_change(self, event=None):
        selected_name = self.voice_var.get()
        for name, vid in self.speaker.get_voices():
            if name == selected_name:
                self.speaker.set_voice(vid)
                break

    def _on_rate_change(self, val):
        self.speaker.rate = int(val)

    def _test_voice(self):
        if self.speaker.get_engine_name() == "none":
            self._log_system("No TTS engine available.", "warn")
            return
        was_enabled = self.speaker.enabled
        self.speaker.enabled = True
        self.speaker.speak("Hello, this is a voice test.")
        self.speaker.enabled = was_enabled

    def _toggle_tts(self):
        if self.speaker.get_engine_name() == "none":
            self._log_system(
                "No TTS engine found.\n"
                "Windows: pip install pyttsx3\n"
                "Linux:   sudo apt install espeak-ng", "warn")
            return
        self.speaker.enabled = not self.speaker.enabled
        if self.speaker.enabled:
            self.tts_btn.config(text="VOICE: ON",  fg=self.GREEN, bg=self.PANEL)
            self._log_system("Voice output ON  (engine: " + self.speaker.get_engine_name() + ")", "muted")
        else:
            self.tts_btn.config(text="VOICE: OFF", fg=self.MUTED, bg=self.PANEL)
            self._log_system("Voice output OFF", "muted")

    # ── services ─────────────────────────────
    def _start_services(self):
        if CAM_OK:
            self.camera.start()
            if self.camera.running:
                self.cam_lbl.config(text="CAM: ON", fg=self.GREEN)
                self._update_camera()
            else:
                self.cam_lbl.config(text="CAM: ERROR", fg=self.RED)
                self._log_system("Camera found but failed to open.", "warn")
        else:
            self.cam_lbl.config(text="CAM: NONE", fg=self.MUTED)
            self.face_status.config(text="NO CAMERA CONNECTED", fg=self.MUTED)
            self._log_system("No camera detected. System running without camera.", "muted")

        if MIC_AVAILABLE and SPEECH_OK:
            self.voice.start()
            self.mic_lbl.config(text="MIC: ON", fg=self.GREEN)
            self.mic_status.config(text="Mic: Listening...", fg=self.GREEN)
        else:
            self.mic_lbl.config(text="MIC: ON", fg=self.GREEN)
            self.mic_status.config(text="Mic: Listening...", fg=self.GREEN)
            self.voice.start()

        if not REQUESTS_OK:
            self._log_system("requests not installed. Run: pip install requests", "warn")

        self._log_system(
            "System online. Using llama3 via Ollama.\n"
            "Make sure ollama is running:  ollama serve", "muted")

    def _update_camera(self):
        if not CAM_OK or not self.camera.running:
            return
        if self.camera.frame is not None:
            try:
                from PIL import Image, ImageTk
                img   = Image.fromarray(cv2.cvtColor(self.camera.frame, cv2.COLOR_BGR2RGB)).resize((256,192))
                photo = ImageTk.PhotoImage(img)
                self.cam_canvas.create_image(0, 0, anchor=tk.NW, image=photo)
                self.cam_canvas._photo = photo
            except Exception:
                pass
            self.face_status.config(
                text="FACE DETECTED" if self.camera.face_detected else "NO FACE DETECTED",
                fg=self.GREEN if self.camera.face_detected else self.RED)
        self.after(80, self._update_camera)

    def _draw_cam_placeholder(self):
        self.cam_canvas.create_rectangle(0, 0, 256, 192, fill="#000")
        self.cam_canvas.create_text(128, 96, text="No Camera Feed",
                                    fill=self.MUTED, font=("Consolas", 10), justify=tk.CENTER)

    def _poll_queue(self):
        try:
            while True:
                kind, data = self.msg_q.get_nowait()
                if kind == "voice":
                    self._process_input(data, source="You (voice)")
                elif kind == "error":
                    self._log_system("Mic error: " + data, "warn")
        except queue.Empty:
            pass
        self.after(200, self._poll_queue)

    def _send_text(self):
        if self.busy:
            return
        text = self.entry.get().strip()
        if not text:
            return
        self.entry.delete(0, tk.END)
        self._process_input(text, source="You")

    def _quick_send(self, cmd):
        if self.busy:
            return
        self.entry.delete(0, tk.END)
        self.entry.insert(0, cmd)
        self._send_text()

    def _clear_chat(self):
        self.chat.config(state=tk.NORMAL)
        self.chat.delete("1.0", tk.END)
        self.chat.config(state=tk.DISABLED)
        self.history = [{"role": "system", "content": SYSTEM_PROMPT}]

    def _process_input(self, text, source="You"):
        if self.busy:
            return
        self.busy = True
        self.send_btn.config(state=tk.DISABLED, text="...")

        user_content = text
        if re.search(r'\b(see me|visible|camera|who am i|identify)\b', text.lower()):
            face_state   = "FACE DETECTED" if (CAM_OK and self.camera.face_detected) else "NO FACE DETECTED"
            user_content = text + "\n[Camera status: " + face_state + "]"

        self.history.append({"role": "user", "content": user_content})
        self._append("\n" + source + ":  ", "you")
        self._append(text + "\n", "response")
        self._append("System:  ", "system")

        def run():
            buf = []
            def on_chunk(chunk):
                buf.append(chunk)
                self.after(0, self._append, chunk, "response")
            full = query_ollama(self.history, on_chunk)
            self.history.append({"role": "assistant", "content": full})
            self.speaker.speak(full)
            self.after(0, self._finish)

        threading.Thread(target=run, daemon=True).start()

    def _finish(self):
        self._append("\n", "response")
        self.busy = False
        self.send_btn.config(state=tk.NORMAL, text="SEND")

    def _append(self, text, tag="response"):
        self.chat.config(state=tk.NORMAL)
        self.chat.insert(tk.END, text, tag)
        self.chat.see(tk.END)
        self.chat.config(state=tk.DISABLED)

    def _log_system(self, text, tag="muted"):
        self._append("\n[SYSTEM]  " + text + "\n", tag)

    def _on_close(self):
        self.camera.stop()
        self.voice.stop()
        self.speaker.stop()
        self.destroy()


