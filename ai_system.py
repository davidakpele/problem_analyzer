class Speaker:
    """
    TTS engine with voice selection.
    - Windows: pyttsx3 (SAPI) - lists all installed Windows voices
    - Linux:   espeak-ng / espeak subprocess
    Each call spawns a fresh process so nothing ever gets stuck.
    """

    def __init__(self):
        self.enabled  = False
        self._queue   = queue.Queue()
        self._engine  = self._detect_engine()
        self.voices   = []      # list of (display_name, voice_id)
        self.voice_id = None    # selected voice id
        self.rate     = 155
        self._load_voices()
        threading.Thread(target=self._worker, daemon=True).start()

    def _detect_engine(self):
        import shutil
        if shutil.which("espeak-ng"):  return "espeak-ng"
        if shutil.which("espeak"):     return "espeak"
        if TTS_OK:                     return "pyttsx3"
        return None

    def _load_voices(self):
        self.voices = []
        if self._engine == "pyttsx3" and TTS_OK:
            try:
                e   = pyttsx3.init()
                raw = e.getProperty("voices")
                e.stop()
                for v in raw:
                    name = v.name if v.name else v.id.split("\\")[-1]
                    self.voices.append((name, v.id))
                if self.voices:
                    self.voice_id = self.voices[0][1]
            except Exception:
                pass
        elif self._engine in ("espeak-ng", "espeak"):
            self.voices = [
                ("English (default)", "en"),
                ("English male 1",    "en+m1"),
                ("English male 2",    "en+m2"),
                ("English male 3",    "en+m3"),
                ("English female 1",  "en+f1"),
                ("English female 2",  "en+f2"),
                ("English (slow)",    "en+m4"),
            ]
            self.voice_id = "en"

    def _worker(self):
        while True:
            item = self._queue.get()
            if item is None:
                break
            text, voice_id, rate = item
            if not self.enabled or not self._engine:
                continue
            try:
                self._do_speak(text, voice_id, rate)
            except Exception:
                pass

    def _do_speak(self, text, voice_id, rate):
        import subprocess, sys
        clean = re.sub(r'\s+', ' ', text).strip()
        if not clean:
            return
        if self._engine in ("espeak-ng", "espeak"):
            cmd = [self._engine, "-s", str(rate), "-a", "200"]
            if voice_id:
                cmd += ["-v", voice_id]
            cmd.append(clean)
            subprocess.run(cmd, stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL)
        elif self._engine == "pyttsx3":
            vid    = voice_id or ""
            script = (
                "import pyttsx3,sys\n"
                "e=pyttsx3.init()\n"
                "e.setProperty('rate'," + str(rate) + ")\n"
                "e.setProperty('volume',1.0)\n"
                "if sys.argv[2]:e.setProperty('voice',sys.argv[2])\n"
                "e.say(sys.argv[1])\n"
                "e.runAndWait()\n"
            )
            subprocess.run(
                [sys.executable, "-c", script, clean, vid],
                stdout=subprocess.DEVNULL,
                stderr=subprocess.DEVNULL,
                timeout=60
            )

    def speak(self, text):
        if self.enabled and self._engine:
            self._queue.put((text, self.voice_id, self.rate))

    def set_voice(self, voice_id):
        self.voice_id = voice_id

    def get_voices(self):
        return self.voices

    def get_engine_name(self):
        return self._engine or "none"

    def stop(self):
        self._queue.put(None)


