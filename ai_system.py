def query_ollama(messages, callback_chunk):
    if not REQUESTS_OK:
        err = "requests library not installed. Run: pip install requests"
        callback_chunk(err)
        return err

    payload = {"model": OLLAMA_MODEL, "messages": messages, "stream": True}
    full_response = []
    try:
        with requests.post(OLLAMA_URL, json=payload, stream=True, timeout=120) as resp:
            if resp.status_code != 200:
                err = "Ollama error: " + str(resp.status_code) + " - Is ollama running?"
                callback_chunk(err)
                return err
            for raw_line in resp.iter_lines():
                if not raw_line:
                    continue
                try:
                    data  = json.loads(raw_line)
                    chunk = data.get("message", {}).get("content", "")
                    if chunk:
                        full_response.append(chunk)
                        callback_chunk(chunk)
                    if data.get("done"):
                        break
                except json.JSONDecodeError:
                    pass
    except requests.exceptions.ConnectionError:
        err = ("\nCannot connect to Ollama.\n"
               "Open a terminal and run:  ollama serve\nThen try again.")
        callback_chunk(err)
        return err
    return "".join(full_response)


