class Camera:
    def __init__(self):
        self.cap = self.face_detected = None
        self.running = False
        self.frame   = None
        self.face_detected = False
        self.cascade = self.cascade_alt = None
        if CAM_OK:
            self.cascade     = cv2.CascadeClassifier(cv2.data.haarcascades + 'haarcascade_frontalface_default.xml')
            self.cascade_alt = cv2.CascadeClassifier(cv2.data.haarcascades + 'haarcascade_frontalface_alt2.xml')

    def start(self):
        if not CAM_OK:
            return
        self.cap = cv2.VideoCapture(CAM_INDEX)
        if not self.cap.isOpened():
            self.cap = None
            return
        self.cap.set(cv2.CAP_PROP_FRAME_WIDTH,  640)
        self.cap.set(cv2.CAP_PROP_FRAME_HEIGHT, 480)
        self.running = True
        threading.Thread(target=self._loop, daemon=True).start()

    def _loop(self):
        while self.running:
            ret, frame = self.cap.read()
            if not ret:
                time.sleep(0.05)
                continue
            display = cv2.resize(frame, (640, 480))
            gray    = cv2.equalizeHist(cv2.cvtColor(display, cv2.COLOR_BGR2GRAY))
            faces   = self.cascade.detectMultiScale(gray, 1.05, 3, minSize=(60,60), flags=cv2.CASCADE_SCALE_IMAGE)
            if len(faces) == 0 and self.cascade_alt:
                faces = self.cascade_alt.detectMultiScale(gray, 1.05, 3, minSize=(60,60))
            self.face_detected = len(faces) > 0
            for (x, y, w, h) in faces:
                cv2.rectangle(display, (x,y), (x+w,y+h), (0,220,80), 2)
                cv2.putText(display, "ADMIN", (x, y-8), cv2.FONT_HERSHEY_SIMPLEX, 0.65, (0,220,80), 2)
            self.frame = display
            time.sleep(0.03)

    def stop(self):
        self.running = False
        if self.cap:
            self.cap.release()


