#  OLLAMA CLIENT
# ══════════════════════════════════════════════
OLLAMA_URL   = "http://localhost:11434/api/chat"
OLLAMA_MODEL = "llama3"

SYSTEM_PROMPT = """You are a local AI assistant running on the user's machine with no internet access.
You have access to a camera and microphone.

Your behaviour:
- When the user asks if you can see them, you will be told the camera result (face detected or not) and you must respond with YES or NO first, then explain briefly.
- When asked "who am I", always respond that the user is "Admin" with full access.
- For math problems, solve them step by step and show your working.
- For code problems or bugs, analyse the code, identify the issue, and suggest a fix.
- For logic problems or ethical dilemmas (like survival scenarios), reason through all angles - utilitarian, deontological, and practical - then give a clear conclusion.
- For general questions, give a direct, helpful, reasoned answer.
- Keep responses clear and concise. Do not use emojis or special symbols.
- Always respond in plain text only. No markdown, no bullet symbols, no asterisks.
- Use dashes (-) for lists if needed. Use plain text headings in ALL CAPS."""


def query_ollama(messages, callback_chunk):
    """
    Stream a response from Ollama.
    callback_chunk(text) is called for each streamed chunk.
    Returns the full response text.
    """
    if not REQUESTS_OK:
        err = "requests library not installed. Run: pip install requests"
        callback_chunk(err)
        return err

    payload = {
        "model":    OLLAMA_MODEL,
        "messages": messages,
        "stream":   True,
    }

    full_response = []
    try:
        with requests.post(OLLAMA_URL, json=payload, stream=True, timeout=120) as resp:
            if resp.status_code != 200:
                err = "Ollama error: " + str(resp.status_code) + " - Is ollama running? Run: ollama serve"
                callback_chunk(err)
                return err

            for raw_line in resp.iter_lines():
                if not raw_line:
                    continue
                try:
                    data = json.loads(raw_line)
                    chunk = data.get("message", {}).get("content", "")
                    if chunk:
                        full_response.append(chunk)
                        callback_chunk(chunk)
                    if data.get("done"):
                        break
                except json.JSONDecodeError:
                    pass

    except requests.exceptions.ConnectionError:
        err = ("\nCannot connect to Ollama.\n"
               "Make sure Ollama is running by opening a terminal and running:\n"
               "    ollama serve\n"
               "Then try again.")
        callback_chunk(err)
        return err

    return "".join(full_response)


